package fundamentos;

public class ObjetosPrimitivosWrappers {
	public static void main(String[] args) {
		
		Character c = 'a'; //primitivo é char
		Boolean bo = true; //primitivo é boolean
		Byte b = 1; //primitivo é byte
		Short s = 2; //primitivo é short
		Integer i = 3; //primitivo é int
		Long l = 4L; //primitivo é long
		Float f = 5F; //primitivo é float
		Double d = 6.5; ////primitivo é double
		System.out.println(bo.toString().length());
		
	}

}
